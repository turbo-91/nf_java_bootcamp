package org.example;

public class Car {
    String brand;
    String model;
    String color;
    Boolean started;
    int speed;

    public Car(String brand, String model, String color, Boolean started, int speed) {
        this.brand = brand;
        this.model = model;
        this.color = color;
        this.started = started;
        this.speed = speed;
    }
}
